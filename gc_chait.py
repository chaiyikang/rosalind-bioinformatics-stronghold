def parse_fasta(fasta_str):
    ids = []
    sequences = []
    current_seq = []

    for line in fasta_str.strip().splitlines():
        line = line.strip()
        if line.startswith(">"):
            if current_seq:
                sequences.append(''.join(current_seq))
                current_seq = []
            ids.append(line[1:])  # remove '>'
        else:
            current_seq.append(line)
    
    if current_seq:
        sequences.append(''.join(current_seq))

    return ids, sequences

def gcContent(seq):
    count = 0
    for c in seq:
        if c == "G" or c == "C":
            count += 1
    return count / len(seq)

def solution(input):
    ids, sequences = parse_fasta(input)
    maxIndex = 0
    maxPercentage = gcContent(sequences[0])
    for index, seq in enumerate(sequences):
        perc = gcContent(seq)
        if perc > maxPercentage:
            maxPercentage = perc
            maxIndex = index
    return ids[maxIndex], maxPercentage

