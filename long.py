import helpers
from collections import deque

def solution(input: str) -> str:
    sequences = deque(helpers.parse_fasta(input)[1])
    acc = sequences.pop()
    while sequences:
         curr = sequences.pop()
         c1 = overlap(acc, curr)
         if c1 is not None:
              acc = c1
              continue
         c2 = overlap(curr, acc)
         if c2 is not None:
              acc = c2
              continue
         sequences.appendleft(curr)
    return acc

# returns combined string if overlap is more than half
def overlap(s1, s2):
    maxOverlap = min(len(s1), len(s2))
    minOverlap = maxOverlap // 2
    for i in range(maxOverlap, minOverlap, -1):
            if s1[-i:] == s2[:i]:  # Suffix of s1 matches prefix of s2
                return s1[:-i] + s2
    return None

