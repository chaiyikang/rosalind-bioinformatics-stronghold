from collections import defaultdict
oneMers = ["A", "C", "G", "T"]

def allPossibleKmers(n: int):
    # [A, C, G, T]
    # [AA, AC, AG, AT, CA, CC, CG, CT,...]
    if n == 1:
        return oneMers
    else:
        return [
            base + prev
            for base in oneMers
            for prev in allPossibleKmers(n - 1)
        ]
    
def kmerToLexIndex(kmer: str) -> int:
    values = {"A": 0, "C": 1, "G": 2, "T": 3}
    # AAAA -> 0
    # AAAC -> 1
    sum = 0
    for i in range(4):
        sum += values[kmer[i]] * pow(4, 3 - i)
    return sum

    
def solution(input: str):
    sequence = "".join(input.splitlines()[1:])
    counts = [0 for _ in range(len(allPossibleKmers(4)))]
    curr = None
    for i in range(len(sequence) - 4 + 1):
        if curr is None:
            curr = sequence[:4]
        else:
            curr = curr[1:] + sequence[4 + i - 1]
        counts[kmerToLexIndex(curr)] += 1
    print(*counts)

