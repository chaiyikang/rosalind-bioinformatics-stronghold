from helpers import parse_fasta

def solution(input):
    sequence = parse_fasta(input)[1][0]

    def longest(i):
        # print("i: ", i)
        # print(sequence[:i + 1])
        if i == 0:
            return 0
        prefixEnd = None
        suffixStart = None
        if (i + 1) % 2 != 0:
            prefixEnd = int(i / 2 - 1)
            suffixStart = prefixEnd + 2
        else:
            prefixEnd = int((i + 1) / 2 - 1)
            suffixStart = prefixEnd + 1
        while suffixStart <= i and sequence[:prefixEnd + 1] != sequence[suffixStart:i + 1]:
            # print(sequence[:prefixEnd + 1], sequence[suffixStart:i + 1])
            prefixEnd -= 1
            suffixStart += 1
        return prefixEnd + 1

    P = []
    for i in range(len(sequence)):
        P.append(longest(i))
    print(*P)


def solution_kmp_with_comments(input):
    """
    KMP algorithm with detailed comments for understanding
    """
    sequence = parse_fasta(input)[1][0]
    n = len(sequence)
    
    P = [0] * n  # Failure array
    j = 0  # Length of the previous longest proper prefix which is also suffix
    
    # P[0] is always 0, so we start from i=1
    for i in range(1, n):
        # This while loop is the key optimization:
        # If sequence[i] != sequence[j], we don't start over from j=0
        # Instead, we use the failure array to jump to P[j-1]
        while j > 0 and sequence[i] != sequence[j]:
            j = P[j - 1]  # This is where we use previous computations!
        
        # If characters match, we can extend the current prefix-suffix
        if sequence[i] == sequence[j]:
            j += 1
        
        # Store the length of longest proper prefix-suffix for position i
        P[i] = j
    
    print(*P)

def solution_kmp_with_chai(input):
    sequence = parse_fasta(input)[1][0]
    n = len(sequence)
    
    failure = [0] * n  # Failure array
    currPrefixLength = 0  # Length of the previous longest proper prefix which is also suffix
    
    # P[0] is always 0, so we start from i=1
    for i in range(1, n):
        # This while loop is the key optimization:
        # If sequence[i] != sequence[j], we don't start over from j=0
        # Instead, we use the failure array to jump to P[j-1]
        while currPrefixLength > 0 and sequence[i] != sequence[j]:
            currPrefixLength = failure[currPrefixLength - 1]  # This is where we use previous computations!
        
        # If characters match, we can extend the current prefix-suffix
        if sequence[i] == sequence[currPrefixLength]:
            currPrefixLength += 1
        
        # Store the length of longest proper prefix-suffix for position i
        failure[i] = currPrefixLength
    
    print(*failure)


