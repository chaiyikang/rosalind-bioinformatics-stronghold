import helpers
import math

def solution(str):
    _, sequences = helpers.parse_fasta(str)
    shortest = min(sequences, key=len)

    def tryLength(length):
        for start in range(len(shortest) - length + 1):
            ss = shortest[start:start + length]
            # print(ss)
            allMatch = True
            for seq in sequences:
                if not ss in seq:
                    allMatch = False
                    break
            if allMatch:
                return ss
        return ""

    low = 1
    high = len(shortest)
    while low < high:
        mid = math.ceil(low + (high - low) / 2)
        if not tryLength(mid):
            high = mid - 1
        else:
            low = mid
    return tryLength(low)



